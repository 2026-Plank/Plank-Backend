const create = async ({ senderId, receiverId, message }) => {
  await execute(
    `INSERT INTO chats (senderId, receiverId, message, timestamp) VALUES (:senderId, :receiverId, :message, SYSDATE)`,
    { senderId, receiverId, message }
  );
  const result = await execute(
    `SELECT id AS "id", senderId AS "senderId", receiverId AS "receiverId", message AS "message",
            timestamp AS "timestamp", deletedAt AS "deletedAt"
     FROM chats
     WHERE senderId = :senderId AND receiverId = :receiverId
     ORDER BY timestamp DESC, id DESC
     FETCH FIRST 1 ROWS ONLY`,
    { senderId, receiverId }
  );
  return result.rows[0] || null;
};

const find = async (filter) => {
  const keys = Object.keys(filter || {});
  const binds = {};
  const clauses = [];
  keys.forEach((key) => {
    if (key === 'or') return;
    clauses.push(`${key} = :${key}`);
    binds[key] = filter[key];
  });

  let sql = `SELECT id AS "id", senderId AS "senderId", receiverId AS "receiverId",
                    CASE WHEN deletedAt IS NULL THEN message ELSE '삭제된 메시지입니다.' END AS "message",
                    timestamp AS "timestamp", deletedAt AS "deletedAt"
             FROM chats`;
  if (filter.or) {
    const orClauses = filter.or.map((cond, index) => Object.keys(cond).map((key) => {
      binds[`or_${index}_${key}`] = cond[key];
      return `${key} = :or_${index}_${key}`;
    }).join(' AND '));
    sql += ` WHERE (${orClauses.join(' OR ')})`;
  } else if (clauses.length) {
    sql += ` WHERE ${clauses.join(' AND ')}`;
  }
  sql += ' ORDER BY timestamp, id';

  const result = await execute(sql, binds);
  return result.rows;
};

const search = async (userId, query) => {
  const sql = `SELECT id AS "id", senderId AS "senderId", receiverId AS "receiverId", message AS "message",
                      timestamp AS "timestamp", deletedAt AS "deletedAt"
               FROM chats
               WHERE deletedAt IS NULL
                 AND (senderId = :userId OR receiverId = :userId)
                 AND LOWER(message) LIKE LOWER(:query)
               ORDER BY timestamp`;
  const result = await execute(sql, { userId, query: `%${query}%` });
  return result.rows;
};

