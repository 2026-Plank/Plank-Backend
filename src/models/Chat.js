const createGroupMessage = async ({ groupId, senderId, message }) => {
  await execute(
    `INSERT INTO chat_group_messages (groupId, senderId, message, timestamp) VALUES (:groupId, :senderId, :message, SYSDATE)`,
    { groupId, senderId, message }
  );
  const result = await execute(
    `SELECT gm.id AS "id", gm.groupId AS "groupId", gm.senderId AS "senderId", u.name AS "senderName",
            gm.message AS "message", gm.timestamp AS "timestamp", gm.deletedAt AS "deletedAt"
     FROM chat_group_messages gm
     JOIN users u ON u.id = gm.senderId
     WHERE gm.groupId = :groupId AND gm.senderId = :senderId
     ORDER BY gm.timestamp DESC, gm.id DESC
     FETCH FIRST 1 ROWS ONLY`,
    { groupId, senderId }
  );
  return result.rows[0] || null;
};

const getGroupMessages = async (groupId) => {
  const result = await execute(
    `SELECT gm.id AS "id", gm.groupId AS "groupId", gm.senderId AS "senderId", u.name AS "senderName",
            CASE WHEN gm.deletedAt IS NULL THEN gm.message ELSE '삭제된 메시지입니다.' END AS "message",
            gm.timestamp AS "timestamp", gm.deletedAt AS "deletedAt"
     FROM chat_group_messages gm
     JOIN users u ON u.id = gm.senderId
     WHERE gm.groupId = :groupId
     ORDER BY gm.timestamp, gm.id`,
    { groupId }
  );
  return result.rows;
};

