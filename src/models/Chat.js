const getConversationSummaries = async (userId) => {
  const directSql = `
    SELECT
      'direct' AS "type",
      u.id AS "userId",
      u.userid AS "userid",
      u.email AS "email",
      u.name AS "name",
      u.profile AS "profile",
      u.presenceStatus AS "presenceStatus",
      latest.message AS "lastMessage",
      latest.timestamp AS "lastTimestamp",
      (
        SELECT COUNT(*)
        FROM chats c
        LEFT JOIN chat_reads r
          ON r.userId = :userId AND r.targetType = 'direct'
         AND r.targetId = CASE WHEN c.senderId = :userId THEN c.receiverId ELSE c.senderId END
        WHERE c.deletedAt IS NULL
          AND c.receiverId = :userId
          AND c.senderId = u.id
          AND c.timestamp > NVL(r.lastReadAt, TO_DATE('1970-01-01', 'YYYY-MM-DD'))
      ) AS "unreadCount"
    FROM (
      SELECT otherId, message, timestamp
      FROM (
        SELECT
          CASE WHEN senderId = :userId THEN receiverId ELSE senderId END AS otherId,
          CASE WHEN deletedAt IS NULL THEN message ELSE '삭제된 메시지입니다.' END AS message,
          timestamp,
          ROW_NUMBER() OVER (
            PARTITION BY CASE WHEN senderId = :userId THEN receiverId ELSE senderId END
            ORDER BY timestamp DESC, id DESC
          ) AS rn
        FROM chats
        WHERE senderId = :userId OR receiverId = :userId
      )
      WHERE rn = 1
    ) latest
    JOIN users u ON u.id = latest.otherId
    ORDER BY latest.timestamp DESC
  `;

  const groupSql = `
    SELECT
      'group' AS "type",
      g.id AS "groupId",
      g.name AS "name",
      latest.message AS "lastMessage",
      latest.timestamp AS "lastTimestamp",
      (
        SELECT COUNT(*)
        FROM chat_group_messages gm
        LEFT JOIN chat_reads r
          ON r.userId = :userId AND r.targetType = 'group' AND r.targetId = g.id
        WHERE gm.groupId = g.id
          AND gm.senderId != :userId
          AND gm.deletedAt IS NULL
          AND gm.timestamp > NVL(r.lastReadAt, TO_DATE('1970-01-01', 'YYYY-MM-DD'))
      ) AS "unreadCount"
    FROM chat_groups g
    JOIN chat_group_members m ON m.groupId = g.id AND m.userId = :userId
    LEFT JOIN (
      SELECT groupId, CASE WHEN deletedAt IS NULL THEN message ELSE '삭제된 메시지입니다.' END AS message, timestamp
      FROM (
        SELECT groupId, message, timestamp, deletedAt,
               ROW_NUMBER() OVER (PARTITION BY groupId ORDER BY timestamp DESC, id DESC) AS rn
        FROM chat_group_messages
      )
      WHERE rn = 1
    ) latest ON latest.groupId = g.id
    ORDER BY NVL(latest.timestamp, g.createdAt) DESC
  `;

  const [direct, groups] = await Promise.all([
    execute(directSql, { userId }),
    execute(groupSql, { userId })
  ]);
  return { direct: direct.rows, groups: groups.rows };
};

