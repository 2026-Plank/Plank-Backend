const upsertRead = async ({ userId, targetType, targetId }) => {
  await execute(`
    MERGE INTO chat_reads r
    USING (SELECT :userId AS userId, :targetType AS targetType, :targetId AS targetId FROM dual) src
    ON (r.userId = src.userId AND r.targetType = src.targetType AND r.targetId = src.targetId)
    WHEN MATCHED THEN UPDATE SET r.lastReadAt = SYSDATE
    WHEN NOT MATCHED THEN INSERT (userId, targetType, targetId, lastReadAt)
      VALUES (:userId, :targetType, :targetId, SYSDATE)
  `, { userId, targetType, targetId });
};

const createGroup = async ({ ownerId, name, memberIds }) => {
  await execute(`INSERT INTO chat_groups (name, ownerId, createdAt) VALUES (:name, :ownerId, SYSDATE)`, { name, ownerId });
  const created = await execute(
    `SELECT id AS "id", name AS "name", ownerId AS "ownerId", createdAt AS "createdAt"
     FROM chat_groups
     WHERE ownerId = :ownerId
     ORDER BY createdAt DESC, id DESC
     FETCH FIRST 1 ROWS ONLY`,
    { ownerId }
  );
  const group = created.rows[0];
  const uniqueMembers = [...new Set([ownerId, ...memberIds.map(Number).filter(Boolean)])];
  await Promise.all(uniqueMembers.map((userId) => addGroupMember({ groupId: group.id, userId })));
  return group;
};

